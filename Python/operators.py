#Identity operators
#'is' -> returns true if the operands are identical
#'is not' -> returns true if operands are NOT identical (do not refer to the same object)
#Example:
x = True 
x is True #this will return true

#Membership operators
#'in' and 'not in'
#Example:
phrase = "Hello World"

if('H' in phrase):
   print('H was found!')
